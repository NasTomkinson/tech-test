import type { Metadata } from "next";
import { Geist_Mono, Inter } from "next/font/google";
import { headers } from "next/headers";
import "./globals.css";

import type { User } from "@/app/api/_mock-data";
import { Appbar } from "@/components/appbar";
import { Header } from "@/templates/composites/header";

const inter = Inter({
  variable: "--font-inter",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

type AuthMeResponse = {
  user: User;
  authenticated: boolean;
};

async function getHeaderUser() {
  const headersList = await headers();
  const host = headersList.get("host");

  if (!host) {
    return null;
  }

  const protocol = headersList.get("x-forwarded-proto") ?? "http";
  const response = await fetch(`${protocol}://${host}/api/auth/me`, {
    cache: "no-store",
  });

  if (!response.ok) {
    return null;
  }

  const data = (await response.json()) as AuthMeResponse;

  if (!data.authenticated) {
    return null;
  }

  return {
    name: `${data.user.firstName} ${data.user.lastName}`,
    avatarUrl: data.user.avatarUrl,
  };
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const headerUser = await getHeaderUser();

  return (
    <html
      lang="en"
      className={`${inter.variable} ${geistMono.variable} h-full w-full antialiased`}
    >
      <body className="md:grid md:grid-cols-[200px_2fr] md:grid-rows-[auto_1fr] min-h-screen">
          {headerUser ? (
            <Header user={headerUser} notificationCount={3} className="md:col-start-2"/>
          ) : null}
        <Appbar className="fixed bottom-0 left-0 md:col-start-1 md:row-start-1 md:row-span-2"/>
        <main className="bg-white md:col-start-2 gap-6 pb-16">
          {children}
        </main>
      </body>
    </html>
  );
}
